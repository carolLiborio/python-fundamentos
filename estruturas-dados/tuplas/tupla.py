#criação de uma tupla com as categorias dos Jedi
categorias = ("Youngling", "Padawan", "Knight", "Master")

#exibir a tupla inteira 
print(categorias)

#exibir o segundo item da tupla 
print(categorias[1])

#usar um índice negativo para exibir o último item da tupla 
print(categorias[-1])

#exibir cada item da tupla usando um loop
for categoria in categorias:
    print(categoria)